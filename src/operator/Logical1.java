package operator;

public class Logical1 {
    static void main(String[] args) {
        System.out.println("&&: AND 연산");
        System.out.println(true && true); // true

        System.out.println("||: OR 연산");
        System.out.println(true || true); // true

        System.out.println("! 연산");
        System.out.println(!true); // false
    }
}
